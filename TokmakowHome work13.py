# Напишите функцию, которая проверяет является ли число(гарантировано целое) палиндромом.
# Число передаётся в качестве параметра. Если число палиндром нужно вернуть из функции true, иначе false.
# «Палиндром» — это число, у которого первая часть цифр равна второй перевернутой части цифр. Например,
# 123321—палиндром(первая часть 123, вторая 321, которая после переворота становится 123),
# 546645 — палиндром, а 421987 — не палиндром.

val = int(input("Введите число  :  "))
def if_palindrom(val):
    val = str(val)
    return val == val[::-1]
print(if_palindrom(val))
