# Напишите функцию, которая возвращает произведение чисел в указанном диапазоне.
# Границы диапазона передаются в качестве параметров. Если границы диапазона перепутаны,
# их нужно поменять местами. Если конец диапазона не передан,
# сделать значение по умолчанию, равное стартовой границе диапазона

start = int(input("Введите начало диапазона :   "))
end = int(input("Введите конец диапазона :  "))
def product_of_num(start, end):
    if start == end:
        return "Введите корректный диапазон"
    if start > end:
       start, end = end, start
    prod = 1
    for i in range(start, end + 1):
         prod *= i
    return prod
print("Произведение чисел диапазона:    ",product_of_num(start, end))
print()

